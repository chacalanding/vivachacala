                    <h2>Surfing </h2>
                    <p>Just to the North of Chacala is the coveted surf break of La Caleta, an often-challenging left point break. La Caleta literally means “Rocky beach” but more accurately would be called “sea-urchin-covered rocky beach”.   Surfers come from all over but do not be lulled by the peaceful paradise of this cove as the wave is unpredictable.  La Caleta is 2km from Chacalilla beach and 2 miles from Chacala beach -- a short boat ride or paddle away.   Beyond La Caleta, surfers can find waves in Lo de Marcos, San Pancho and Sayulita.</p>
