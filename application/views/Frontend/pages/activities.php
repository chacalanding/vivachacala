                    <h2>SUP</h2>
                    <p>Chacala is surrounded by miles of enchanted coastline.  Exploring on a stand-up paddleboard (SUP) is one of the most epic ways to explore the area.  Paddleboards can be rented in the village or your VivaChacala concierge can have them waiting for you at the beach club.  The ocean is usually calmest in the morning before 11am so early expeditions are often best.  Below are some of the best SUP excursions:</p>
